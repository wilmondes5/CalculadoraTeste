
export default class Calculadora{

    static soma(a, b){
        return a + b
    }
    static subtração(a, b){
        return a - b
    }
    static divisão(a, b){
        return a / b
    }
    static mutiplicação(a, b){
        return a * b
    }
    static igualdade(a, b){
        return a == b
    }
    static diferente(a, b){
        return a !== b
    }
}